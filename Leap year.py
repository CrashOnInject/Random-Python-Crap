integer1 = int(input("Enter an integer: "))
integer2 = int(input("Enter a second integer: "))

Sum = integer1 + integer2
print("The sum result is:", Sum)

Difference = integer1 - integer2
print("The difference result is:", Difference)

Product = integer1 * integer2
print("The product result is:", Product)

Average = Sum / 2
print("The average result is:", Average)

Distance = abs(Difference)
print("The distance (absolute value of the difference) is:", Distance)

Maximum = max(integer1, integer2)
print("The maximum number is:", Maximum)